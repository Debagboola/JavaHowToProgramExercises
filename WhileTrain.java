public class WhileTrain{
	public static void main(String[] arg){
		int counter = 1;
			
		while(counter  <= 4){
			int i = 1;
			while(i<= counter){
				System.out.print("*" );
				i = i + 1;
			}
			System.out.println();
			counter ++;
		}

                System.out.println();
                System.out.println();
                System.out.println();

		int counter2 = 4;
			
		while(counter2  > 0){
			int i = 1;
			while(i<= counter2){
				System.out.print("*" );
				i = i + 1;
			}
			System.out.println();
			counter2 --;
		}

                System.out.println();
                System.out.println();
                System.out.println();

		

	}
}